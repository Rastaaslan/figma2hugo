COMPONENT_STYLESHEET_PARTS: tuple[tuple[str, ...], ...] = (
    ("hugo", "assets", "css", "components", "link-grid.css"),
    ("hugo", "assets", "css", "components", "accordion.css"),
    ("hugo", "assets", "css", "components", "carousel.css"),
    ("hugo", "assets", "css", "components", "form.css"),
    ("hugo", "assets", "css", "components", "section-block.css"),
)


class CssGenerator:
    """Génère la feuille de style calculée à partir du modèle canonique.

    Le rôle de ce générateur est volontairement limité :
    - poser le "shell" CSS minimal commun à toutes les sorties ;
    - injecter, si demandé, les presets de composants partagés ;
    - écrire la géométrie absolue et les styles issus de Figma.
    """

    def __init__(self, *, include_component_presets: bool = True) -> None:
        # En sortie Hugo, les presets de composants vivent dans un fichier partagé
        # dédié. En sortie statique, on les réinjecte dans `styles.css`.
        self._include_component_presets = include_component_presets

    def generate(self, page_data: dict[str, Any]) -> str:
        page = page_data.get("page", {})
        sections = page_data.get("sections", [])
        tokens = page_data.get("tokens", {})
        page_width, page_height, page_origin_x, page_origin_y = self._page_geometry(page, sections)
        lines = [
            *self._font_imports(page_data),
            "/* Generated by figma2hugo. */",
            "",
            *self._root_variable_lines(page_width, tokens),
            *self._base_shell_lines(page_width, page_height),
        ]
        if self._include_component_presets:
            lines.extend(self._component_preset_lines())
        flow_cursor_bottom = 0.0
        for section in sections:
            section_lines, flow_cursor_bottom = self._section_rules(
                section,
                page_width,
                page_origin_x,
                page_origin_y,
                flow_cursor_bottom=flow_cursor_bottom,
            )
            lines.extend(section_lines)
        lines.extend(self._responsive_override_lines(page_data))
        return "\n".join(lines) + "\n"

    def _root_variable_lines(self, page_width: int, tokens: dict[str, Any]) -> list[str]:
        lines = [
            ":root {",
            f"  --page-max-width: {page_width}px;",
            "  --page-shell-gutter: clamp(16px, 3vw, 32px);",
            "  --surface-page: #ffffff;",
            "  --text-color: #171717;",
        ]
        lines.extend(self._token_variables("color", tokens.get("colors", {})))
        lines.extend(self._token_variables("space", tokens.get("spacing", {})))
        lines.extend(self._token_variables("font", tokens.get("typography", {})))
        lines.extend(self._token_variables("shadow", tokens.get("shadows", {})))
        lines.extend(self._token_variables("radius", tokens.get("radii", {})))
        lines.append("}")
        return lines

